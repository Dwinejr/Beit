<p>
<span class="orange">Curso Preparatório para Exames:</span><br />
Os Cursos Preparatórios para os exames TOEFL e IELTS  têm como base os melhores materiais disponíveis no mercado, adaptados para a Metodologia Beit de Ensino, o que aperfeiçoa a qualidade dos cursos.
<br />
<br />
<span class="blue"><strong>TOEFL</strong> (Test of English as a Foreign Language)</span><br />
Tem como objetivo avaliar a capacidade individual de fala e entendimento do inglês americano em nível acadêmico. Imprescindível para os estudantes estrangeiros que querem ingressar nas universidades de um país que tem o inglês como primeiro idioma. Empresas e programas de estudo também podem exigir este teste.
<br />
<br />
<span class="blue"><strong>IELTS</strong> (International English Language Testing System)</span><br />
Tem como objetivo avaliar se o aluno tem condições de utilizar a língua inglesa em estudos ou estágios. Testa-se a capacidade de leitura, fala e compreensão do idioma, em um ambiente acadêmico ou de treinamento.
